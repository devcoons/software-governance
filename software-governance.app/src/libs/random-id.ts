import { randomBytes } from 'node:crypto'

/* ---------------------------------------------------------------------- */

export function randomId(bytes = 32): string {
	const b = randomBytes(bytes)
	const s = b.toString('base64').replace(/\+/g, '-').replace(/\//g, '_').replace(/=+$/g, '')
	return s
}

